<template>
  <component :is="layout">
    <router-view/>
  </component>
</template>
<script>
const defaultLayout = 'defaultLayout';

export default {
  computed: {
    layout() {
      return `${this.$route.meta.layout || defaultLayout}`;
    },
  },
};
</script>
